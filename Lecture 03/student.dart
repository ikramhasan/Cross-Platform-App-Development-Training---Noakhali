// To create a class, we need 3 things
// First we need to take a keyword named [class]
// Then we need to provide a class name is capital letters
// Lastly, we need to provide {}
class Student {
  // To make a method, we need to give it a name first
  // Then a first bracket and second bracket
  show(name, age) {
    print("$name $age");
  }
}
